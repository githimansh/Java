class CharToNumber {
    public static void main(String[] args) {

        char ch = 'A';
        char ch1 = 'a';


        int num = ch;
        int num1 = ch1; 

        System.out.println("Character = " + ch);
        System.out.println("ASCII Value = " + num);
        System.out.println("Character = " + ch1);
        System.out.println("ASCII Value = " + num1);

    }
}